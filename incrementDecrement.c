#include<stdio.h>

int main()
{
    int a= 10;
    int b= 10;
    int c = 10;
    int d= 10;

    a++; //increment
    ++b; //increment

    c--; //decrement
    --d; //decrement

    printf("%d\n",a); 
    printf("%d\n",b);
    printf("%d\n",c);
    printf("%d\n",d);


    return 0;

}